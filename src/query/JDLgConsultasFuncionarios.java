    private void jBtnConsutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsutarActionPerformed
        // TODO add your handling code here:
  
         controle = new  ControleFuncionario();
        FuncionarioDAO funDAO = new FuncionarioDAO();
          
        if (jTxtNome.getText().equals("") && jCBoSexo.getSelectedIndex() == 4) {
             List lista = funDAO.listAll();
        controle.setList(lista);
        jTable1.setModel(controle);
            
        } else if (!jTxtNome.getText().equals("") && jCBoSexo.getSelectedIndex() < 4) {
             List lista = funDAO.listNomeSexo(jTxtNome.getText(),jCBoSexo.getSelectedIndex());
        controle.setList(lista);
        jTable1.setModel(controle);
        }else if (!jTxtNome.getText().equals("") && jCBoSexo.getSelectedIndex() == 4) {
            List lista = funDAO.listNome(jTxtNome.getText());
        controle.setList(lista);
        jTable1.setModel(controle);
        }else{
        List lista = funDAO.listSexo(jCBoSexo.getSelectedIndex());
        controle.setList(lista);
        jTable1.setModel(controle);
        }
         
    }//GEN-LAST:event_jBtnConsutarActionPerformed

