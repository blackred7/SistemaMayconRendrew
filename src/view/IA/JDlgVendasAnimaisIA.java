                 VendasDAO vdao = new VendasDAO();    
                  
        
         VenAni.setValorUnitarioMr(Util.strDouble(JTxtVarU.getText()) );
        VenAni.setQuantidadeUnitariaMr(Util.strInt(jTxtQuantU.getText()) );      
             VenAni.setAnimaisMr((AnimaisMr) jCboFkAni.getSelectedItem());
