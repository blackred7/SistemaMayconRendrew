    private void jBtnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnOkActionPerformed
        
         VendasAnimaisMr vemAni = viewBean();
        VendasAnimaisDAO vemAniDAO = new VendasAnimaisDAO();
    
        
        
         if (getTitle().toUpperCase().substring(0, 1).equals("I")) {
             vemAniDAO.insert(vemAni);
             jDLgVendas.vendasControle.addBean(vemAni);
       
        
             
        } else {    
             vemAniDAO.update(vemAni);
                         jDLgVendas.vendasControle.updateBean(jDLgVendas.getSelectedRowProd(), vemAni);

        }
        setVisible(false);
//       
        
        

        this.dispose();
    }//GEN-LAST:event_jBtnOkActionPerformed

